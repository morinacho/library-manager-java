    private void deleteAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteAcceptActionPerformed
        // TODO add your handling code here:
        Files partnerfile = new Files("partners");
        List <Partner> partners;
        try {
            partners = new ArrayList<>(partnerfile.readFile("partners"));
            for(int i = 0; i < partners.size(); i++){
                if(partners.get(i).search(this.dni) != null){
                    partners.remove(i);
                    partnerfile.writeFile(partners);
                }
            }
            this.dispose();
        } 
        catch (IOException ex) {
            Logger.getLogger(PartnerDelete.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deleteAcceptActionPerformed

