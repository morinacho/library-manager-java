    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        System.out.println(this.idbook);
        Files bookfile = new Files("books");
        List <Book> books;
        try {
            books = new ArrayList<>(bookfile.readFile("books"));
            for(int i = 0; i < books.size(); i++){
                if(books.get(i).search(this.idbook) != null){
                    books.remove(i);
                    bookfile.writeFile(books);
                }
            }
            this.dispose();
        } 
        catch (IOException ex) {
            Logger.getLogger(PartnerDelete.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deleteActionPerformed

